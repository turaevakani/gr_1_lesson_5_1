public class Boss {
    private int health;
    private int damage;
    private String setTypeOfDefence;

    public int getHealth() {
        return health;
    }

    public void setHealth(int health) {
        this.health = health;
    }

    public int getDamage() {
        return damage;
    }

    public void setDamage(int damage) {
        this.damage = damage;
    }

    public String getTypeOfDefence() {
        return setTypeOfDefence;
    }

    public void setTypeOfDefence(String typeOfProtection) {
        this.setTypeOfDefence = typeOfProtection;
    }
}
